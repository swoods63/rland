      What about bringing children into Canada?
      <h4 class="answer">
      If you are travelling with children, you should carry identification for each child. Divorced parents who share custody of their children should carry copies of the legal custody documents. Adults who are not parents or guardians should have written permission from the parents or guardians to supervise the children. When travelling with a group of vehicles, parents or guardians should travel in the same vehicle as the children when arriving at the border.<br><br>
      Customs officers are looking for missing children and may ask questions about the children who are travelling with you
      </h4>
    </li>

