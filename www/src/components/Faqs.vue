      What should I know before entering into Canada?
      <h4 class="answer">
      The Canada / US border is reputed to be the friendliest border in the world. Here are some tips to help make your crossing quick and easy:<br><br>
      <b class="b">Identification</b><br>
      US Citizens and legal residents of the United States do not need passports or visas to enter Canada as visitors. You should however carry some identification such as birth, voter's or naturalization certificate which shows your citizenship.<br><br>
      <b class="b">Children</b><br>
      Canada Customs and Immigration officers are members of Child Find International. Anyone travelling with children not their own may be required to show written permission from the child's parent(s) and proper identification (birth certificate).<br><br>
      <b class="b">Other Countries</b><br>
      Visitors from countries other than the US require a valid passport and in some cases a visitor's visa. Visas are issued by Canadian Immigration offices, embassies and consulates.<br><br>
      <b class="b" >Making Declarations</b><br>
      Canadian Customs officials will ask your place of residence (country and city) the purpose of your visit to Canada, how long you intend to stay, and if you have anything to declare. You may be asked specifically if you are bringing alcohol, tobacco, fruit, vegetables, animals or items made from animal by-products, firearms or personal protection devices into Canada.<br><br>
      Gifts up to $60 (Canadian funds) each in value are duty free provided they do not contain alcohol, tobacco or advertising material.<br><br>
      As long as you meet the age requirement set by the province where you enter Canada (19 in Ontario), you may import 1.5 litres of wine or 1.14 litres (40 ounces) of liquor OR 24 x 355 ml cans or bottles of beer or ale. Those over 18 years of age may bring 200 cigarettes, 50 cigars, 200 grams of manufactured tobacco and 200 tobacco sticks.<br><br>
      <b class="b">Pets</b><br>
      Dogs and cats accompanying their owners from the U.S. must have current (within 36 months) rabies vaccination certificates. Owners from other countries who wish to bring their pets should contact the nearest Canadian consulate or embassy.<br><br>
      <b class="b">Boaters</b><br>
      Pleasure craft may enter Canada by trailer or under their own power and stay for a period of up to 12 months. The required Entry Permit is obtainable from Customs at port-of-entry. All boats powered by motors 10 HP or over must be licensed and the license number clearly indicated on the bow of the vessel. Boat licenses from outside Ontario are acceptable.<br><br>
      <b class="b">Criminal Records</b><br>
      Individuals with criminal records wishing to enter Canada can apply for a waiver. This should be done 3 - 4 weeks in advance of the intended visit. It should be noted that Driving Under the Influence convictions are considered a criminal offense in Canada. Individuals with criminal records without waivers can be refused entry. Contact the nearest port of entry Canada Immigration office for more information.<br><br>
      <b class="b">Your Dollar Goes Farther</b><br>
      US dollars and several foreign currencies enjoy a premium rate of exchange in Canada.<br><br>
      <b class="b">Goods and Services Tax (GST)</b><br>
      As a visitor to Canada you can claim a refund of 7% on every dollar spent while in the country on accommodation and most goods you take home.<br><br>
