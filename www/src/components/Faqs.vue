<h2><strong>PROPERTY RIGHTS, HISTORICAL CONTEXT</strong></h2>

    <li>
      What do you mean when you talk about a shoreline reservation?
      <h4 class="answer">
      All waterfront property in Northern Ontario has a standard 66 foot shoreline reservation, unless otherwise transferred to the Deed. This reservation can be as deep as 200 feet on some properties. This part of the property is owned and controlled by the Ministry of Natural Resources. No permanent development or shoreline development is allowed on this reservation. In most cases the shoreline reservation may be purchased from the Ministry of Natural Resources.
      </h4>
    </li>

    <li>
      What is the difference between mineral rights and surface rights?
      <h4 class="answer">
      There are parcels of land in Ontario where landowners may own the surface rights but do not own the mineral rights in, on or below their land. The Ontario Mining Act provides a statutory right to stake mining claims on Crown mineral rights and to conduct assessment work on the mining claims even if the surface rights are privately owned.<br><br>
      Section 78 of the Mining Act requires that the holder of a mining claim notify the surface rights owner of their intention to perform assessment work on that claim. This notice is only given once, prior to the commencement of assessment work.<br><br>
      The Mining Act also provides for a process of compensation if there is damage to the property of the surface rights holder. Should there be any damage to surface rights, the Mining and Lands Commissioner may award compensation. There have been approximately fourteen applications filed with the Commissioner on compensation issues over the last ten years.<br><br>
      Section 32 of the Mining Act prohibits prospecting and staking on that part of a lot where there is a dwelling, cemetery, public building, garden, orchard or crops that may be damaged. Prospecting and staking can only occur in these areas with the consent of the surface rights holder or by order of the Provincial Mining Recorder or Commissioner. If consent is not given it may mean that the mining claim is invalid or that parts of the mining claim may be excluded.
      </h4>
    </li>

     <li>
