      What about selling my property that I own in Canada?
      <h4 class="answer">
        When a non-resident sells Canadian real estate, he/she is required to pay the appropriate amount of taxes on any capital gain. The normal Canadian tax rates will be applied to 50% of the gain. However, a non-resident is required to pay an estimate of the tax before the sale, an amount equal to 25% of the gain. This amount is to be retained by the seller's lawyer until such time as a clearance certificate is received from the Canada Revenue Agency (CRA) in connection with the sale of the property. Upon payment, the CRA will issue a clearance certificate to the seller, but not until there has been a contract of purchase and sale with all subjects (conditions) removed. The wait for the certificate is usually 6-8 weeks. The non-resident seller should file a Canadian income tax return for the year in which the sale occurs and should expect to receive a refund of a portion of the taxes paid.<br><br>

