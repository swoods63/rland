      Can I bring firearms into Canada?
      <h4 class="answer">
      You may bring a hunting rifle or shotgun and up to 200 rounds of ammunition into Ontario as a visitor, but you must be 18 years of age or older to do so and the firearms must be for hunting or competitions use. You must be at least 18 years of age to bring firearms into Canada.<br><br>
      You must either have a Canadian Possession and Acquisition License (PAL) or you have to register your firearms at the Canadian border. You are able to complete pre-registration forms before you cross the border which will make it easier and less time consuming. The permit fee is $50.00 (Cdn) per person for a temporary registration permit and the permit is valid for 12 months. Call toll-free 1-800-731-4000 to get a Firearms Declaration Form. Easiest way to obtain a Canadian PAL (possession and Acquisition License) prior to importing guns into Canada. If you have a PAL, you do not have to fill out registration forms and there is only a one time fee.<br><br>
      You cannot import prohibited firearms. Fully automatic weapons, all hand guns and guns of less than 66 cm (26 inches) in length are prohibited by law.<br><br>
      Restricted firearms such as pistols or revolvers may be temporarily imported if you have an Authorization to Transport (ATT) in advance from a Canadian Chief Firearms Officer.<br><br>
      Seasonal residents may import restricted firearms, but must have a Possession and Acquisition License or a valid Firearms Acquisition Certificate in addition to an ATT.<br><br>
      For more info on importing firearms into Canada: 
